void main() {
  
  //declaring variables
  String name = 'Michael'; //must be in quotes
  String address ="Kenya"; //must end with ;
  num age = 22; //whole numbers
  num height = 5.11; //decimal numbers
  bool isMarried = false; //boolean value
  var x; //the keyword var can be used to declare variables as well, in this case the variable is x
  x = 77; //you can also declare the value of x in the previous line as var x = 77;
  
  //printing variables value
  print("Name is $name");
  print("Address is $address");
  print("Age is $age");
  print("Height is $height");
  print("Marriage status is $isMarried");
  print("The value of x is $x");

}